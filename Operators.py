a = 10
b = 13


a=b         #Assigns right value to left
print(a)    #a=13

a=10         #Re-assigning
print(a)     #a=10

a+=b        #a+b
print(a)    #a=23

a-=b        #a-b here a=23
print(a)    #a=10

a*=b        #a*b here a=10
print(a)    #a=130

a%=b        #130 divide ny 10 remainder 0
print(a)    #a=0

a=2  #Re-assigning

a**=b       #2 raised to 13
print(a)    #a=8192

a//=b       #8192 divide by 13
print(a)    #a=630